<script setup>
import { onMounted, ref } from "vue";
import { useRouter } from "vue-router";

const router = useRouter();
let invoices = ref([]);
let searchInvoice = ref([]);

onMounted(async () => {
    getInvoices();
});

const getInvoices = async () => {
    await axios.get("/api/invoices").then((response) => {
        invoices.value = response.data.data;
    });
};

const search = async () => {
    await axios
        .get(`/api/invoices/search?s=${searchInvoice.value}`)
        .then((response) => {
            invoices.value = response.data.data;
        });
};

const newInvoice = async () => {
    await axios.get("/api/invoices/create").then(() => {
        router.push("/create");
    });
};

const onShow = (id) => {
    router.push(`/show/${id}`);
};
</script>

<template>
    <div class="container">
        <!--==================== INVOICE LIST ====================-->
        <div class="invoices">
            <div class="card__header">
                <div>
                    <h2 class="invoice__title">Invoices</h2>
                </div>
                <div>
                    <a class="btn btn-secondary" @click="newInvoice">
                        New Invoice
                    </a>
                </div>
            </div>

            <div class="table card__content">
                <div class="table--filter">
                    <span class="table--filter--collapseBtn">
                        <i class="fas fa-ellipsis-h"></i>
                    </span>
                    <div class="table--filter--listWrapper">
                        <ul class="table--filter--list">
                            <li>
                                <p
                                    class="table--filter--link table--filter--link--active"
                                >
                                    All
                                </p>
                            </li>
                            <li>
                                <p class="table--filter--link">Paid</p>
                            </li>
                        </ul>
                    </div>
                </div>

                <div class="table--search">
                    <div class="table--search--wrapper">
                        <select class="table--search--select" name="" id="">
                            <option value="">Filter</option>
                        </select>
                    </div>
                    <div class="relative">
                        <i class="table--search--input--icon fas fa-search"></i>
                        <input
                            class="table--search--input"
                            type="text"
                            placeholder="Search invoice"
                            v-model="searchInvoice"
                            @keyup="search()"
                        />
                    </div>
                </div>

                <div class="table--heading">
                    <p>ID</p>
                    <p>Date</p>
                    <p>Number</p>
                    <p>Customer</p>
                    <p>Due Date</p>
                    <p>Total</p>
                </div>

                <!-- item 1 -->
                <div
                    class="table--items"
                    v-if="invoices.length > 0"
                    v-for="invoice in invoices"
                    :key="invoice.id"
                >
                    <a
                        href="#"
                        class="table--items--transactionId"
                        @click="onShow(invoice.id)"
                        >#{{ invoice.id }}</a
                    >
                    <p>{{ invoice.date }}</p>
                    <p>#{{ invoice.number }}</p>
                    <p>
                        {{ invoice.customer.fullname }}
                    </p>
                    <p>{{ invoice.due_date }}</p>
                    <p>$ {{ invoice.total }}</p>
                </div>

                <div class="table--items text-center" v-else>
                    <p>Invoice not found</p>
                </div>
            </div>
        </div>
    </div>
</template>
